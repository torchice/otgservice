    private void cbjenisActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cbjenisActionPerformed
        // TODO add your handling code here:
        image = null;
        if (cbjenis.getSelectedItem().toString().equals("Komputer")){
            kode = "TKO";
        }
        else if (cbjenis.getSelectedItem().toString().equals("Motor")){
            kode = "TMT";
        }
        else if (cbjenis.getSelectedItem().toString().equals("Mobil")){
            kode = "TMB";
        }
        else if (cbjenis.getSelectedItem().toString().equals("Mesin")){
            kode = "TME";
        }
        
        cbnama.removeAllItems();
        String SQL = "SELECT KODETEKNISI, NAMATEKNISI, ALAMAT, NOTELP, FOTOPRIBADI FROM TEKNISI WHERE KODETEKNISI LIKE '" + kode + "%' AND KOTA LIKE '%" + kota + "%'";
        ResultSet rs = OTGService.executeQuery(SQL);

        try {
            while(rs.next()) {
                String kodetek = rs.getString(1);
                String nama = rs.getString(2);
                String alamat = rs.getString(3);
                String telp = rs.getString(4);
                Blob foto = rs.getBlob(5);
                InputStream in = foto.getBinaryStream();
                try {
                    image = ImageIO.read(in);
                } catch (IOException ex) {
                    Logger.getLogger(FramePilihTeknisi.class.getName()).log(Level.SEVERE, null, ex);
                }
                teknisi.add(new ClassPilihTeknisi(kodetek, nama, alamat, telp, image));
                cbnama.addItem(teknisi.get(teknisi.size()-1).getNama());
            }
        } 
        catch (SQLException ex) {
            Logger.getLogger(Login.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_cbjenisActionPerformed

