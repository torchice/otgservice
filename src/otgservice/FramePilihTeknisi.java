    private void cbnamaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cbnamaActionPerformed
        // TODO add your handling code here:
        foto = null;
        tbalamat.setText("");
        tbtelp.setText("");
        for (int i = 0; i < teknisi.size(); i++) {
            if (cbnama.getItemCount()>0){
                if (cbnama.getSelectedItem().equals(teknisi.get(i).getNama())){
                    foto = teknisi.get(i).getFoto();
                    tbalamat.setText(teknisi.get(i).getAlamat());
                    tbtelp.setText(teknisi.get(i).getTelp());
                    kodetek = teknisi.get(i).getKode();
                }
            }
        }
    }//GEN-LAST:event_cbnamaActionPerformed

